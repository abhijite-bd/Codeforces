using namespace std;
int main()
{
    ll t, m, n, i, j, l, w, ans, mx;
    cin >> t;
    while (t--)
    {
        cin >> n >> m;
        ll x;
        mx = INT_MIN;
        for (i = 0; i < n; i++)
        {
            for (j = 0; j < m; j++)
            {
                cin >> x;
                if (x > mx)
                {
                    mx = x;
                    l = i;
                    w = j;
                }
            }
        }
        ans = max(l + 1, n - l) * (max(w + 1, m - w));
        cout << ans << endl;
    }
    return 0;
}