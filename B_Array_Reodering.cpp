int main()
{
    int t, n, ans, i, j, k, l;
    cin >> t;
    while (t--)
    {
        cin >> n;
        vector<int> v, odd;
        for (i = 0; i < n; i++)
        {
            cin >> l;
            if (l % 2)
                odd.pb(l);
            else
                v.pb(l);
        }
        for (i = 0; i < odd.size(); i++)
        {
            v.pb(odd[i]);
        }
        ans = 0;
        for (i = 0; i < v.size() - 1; i++)
        {
            for (j = i + 1; j < v.size(); j++)
            {
                if (gcd(v[i], 2 * v[j]) > 1)
                    ans++;
            }
        }
        cout << ans << endl;
    }
    return 0;
}