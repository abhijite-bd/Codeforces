int main()
{
    ll t, n, ans1, ans2;
    cin >> t;
    while (t--)
    {
        cin >> n;
        // ans1 = n / 6;
        // ans2 = n / 4;
        if (n % 2 == 1 or n == 2)
        {
            cout << -1 << endl;
            continue;
        }
        if (n % 6 == 0)
            ans1 = n / 6;
        else
        {
            if (n % 6 == 4)
                ans1 = n / 6 + 1;
            else
                ans1 = -1;
        }
        if (n % 4 == 0 or n % 4 == 2)
            ans2 = n / 4;
        else
        {
            if (ans1 == -1)
                ans2 == -1;
            else
                ans2 = ans1;
        }
        if (ans2 != -1 and ans1 == -1)
            ans1 = ans2;

        ll total = n / 2;
        ans1 = total / 2;
        ans2 = total / 3;
        if (total % 3 != 0)
            ans2++;

        cout << min(ans1, ans2) << ss << max(ans1, ans2) << endl;
    }
    return 0;
}