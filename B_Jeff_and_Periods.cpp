int main()
{
    int n, i, j, k;
    cin >> n;
    map<int, vector<int>> m;
    for (i = 1; i <= n; i++)
    {
        cin >> k;
        m[k].push_back(i);
    }
    map<int, int> ans;
    for (auto e : m)
    {
        if (e.second.size() == 1)
            ans[e.first] = 0;
        else
        {
            int p = e.second[1] - e.second[0];
            // cout<<p;
            int f = 1;
            for (j = 2; j < e.second.size(); j++)
            {
                if (p != e.second[j] - e.second[j - 1])
                {
                    f = 0;
                    break;
                }
            }
            if (f)
                ans[e.first] = p;
        }
    }
    cout << ans.size() << endl;
    for (auto e : ans)
        cout << e.first << ss << e.second << endl;
    return 0;
}