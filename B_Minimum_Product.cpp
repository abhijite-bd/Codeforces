ll min_v1(ll a, ll b, ll x, ll y, ll n)
{
    ll ax = a - x;
    a -= min(n, ax);
    n -= min(n, ax);
    b -= min(n, b - y);
    return a * b;
}
ll min_v2(ll a, ll b, ll x, ll y, ll n)
{
    ll by = b - y;
    b -= min(by, n);
    n -= min(by, n);
    a -= min(n, a - x);
    return a * b;
}
