    int t, n, m, mn, i, j;
    cin >> t;
    while (t--)
    {
        cin >> n >> m;
        int a[n][m];
        int mins = 0, sum = 0, mn = INT_MAX;
        for (i = 0; i < n; i++)
        {
            for (j = 0; j < m; j++)
            {
                cin >> a[i][j];
                sum += abs(a[i][j]);
                if (a[i][j] < 0)
                    mins++;
                mn = min(mn, abs(a[i][j]));
            }
        }
        if (mins % 2 == 0)
            cout << sum << endl;
        else
            cout << sum - mn * 2 << endl;
    }
    return 0;
