const int N = 2e5 + 10;
vector<int> g[N];
bool vis[N];
int cat[N];
int level[N];
int n, k, ans;
void dfs(int v, int par = -1)
{
    int flag = 1;
    if (vis[v])
        return;
    vis[v] = true;
    level[v] = max(cat[v], level[v]);
    for (auto child : g[v])
    {
        if (child == par)
            continue;
        else
            flag = 0;
        if (cat[child])
            level[child] = level[v] + 1;
        if (k < level[child])
            continue;
        dfs(child, v);
    }
    if (flag)
        ans++;
}
int main()
{
    int m, i, j, l;
    cin >> n >> k;
    for (i = 1; i <= n; i++)
    {
        cin >> cat[i];
    }
    for (i = 0; i < n - 1; i++)
    {
        int v1, v2;
        cin >> v1 >> v2;
        g[v1].push_back(v2);
        g[v2].push_back(v1);
    }
    dfs(1);
    cout << ans << endl;
    return 0;
}