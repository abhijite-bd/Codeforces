using namespace std;
const int N = 510;
char g[N][N];
bool vis[N][N];
vector<pair<int, int>> v;
int n, m;
vector<pair<int, int>> movements = {//move adjacent cells
    {0, 1}, {1, 0}, {-1, 0}, {0, -1}};
bool isvalid(int i, int j)//chk the cell
{
    if (i < n and i >= 0 and j < m and j >= 0)
        return 1;
    return 0;
}
void dfs(int i, int j)
{
    if (vis[i][j])
        return;
    vis[i][j] = 1;
    // cout << i+1 << ss << j+1 << endl;
    v.push_back({i, j});//storing paths
    for (auto move : movements)
    {
        int move_x = i + move.first;
        int move_y = j + move.second;
        if (vis[move_x][move_y])
            continue;
        if (!isvalid(move_x, move_y))
            continue;
        if (g[move_x][move_y] == '#')
            continue;
        // cout << move_x << ss << move_y << endl;
        dfs(move_x, move_y);
    }
}
int main()
{
    int i, j, k, l, x, p;
    cin >> n >> m >> k;
    for (i = 0; i < n; i++)
    {
        for (j = 0; j < m; j++)
        {
            cin >> g[i][j];
        }
    }
    for (i = 0; i < n; i++)
    {
        for (j = 0; j < m; j++)
        {
            if (g[i][j] == '.')
            {
                dfs(i, j);//start from first empty cell
                break;
            }
        }
    }
    for (i = v.size() - k; i < v.size(); i++)
    {
        auto it = v[i];
        g[it.first][it.second] = 'X';//block paths
    }
    for (i = 0; i < n; i++)
    {
        for (j = 0; j < m; j++)
        {
            cout << g[i][j];
        }
        cout << endl;
    }
    return 0;
}