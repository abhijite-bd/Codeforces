ll binary_search2(ll l, ll r, ll a[], ll n)
{
    while (l <= r)
    {
        ll mid = (l + r) / 2;
        // cout << l << ss << r << ss << mid << endl;
        int flag = 0;
        for (ll i = 0; i < n - 1; i++)
        {
            if (abs(a[i] - mid) > abs(a[i + 1] - mid))
            {
                if (a[i] > a[i + 1])
                    flag = 1;
                else
                    flag = 2;
                break;
            }
        }
        if (flag == 0)
            return mid;
        else if (flag == 1)
            l = mid + 1;
        else
            r = mid - 1;
    }
    return -1;
}

